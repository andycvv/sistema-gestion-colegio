package entidad;

public class Grado {
	// atributos privados
	private int idGrado;
	private String nombre;

	// constructor
	public Grado() {
		
	}

	// metodos get/set
	public int getIdGrado() {
		return idGrado;
	}

	public void setIdGrado(int idGrado) {
		this.idGrado = idGrado;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
